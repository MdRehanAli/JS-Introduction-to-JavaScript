// variable Declaration
var tomatoPrice = 25;

// variable Update
tomatoPrice = 30;

// add something to the previous value 
var price1 = 30;
var price2 = 10;

// set a new value 
price1 = 35;

// add some value to the existing value 
price1 = price1 + 10;
console.log(price1);

// addition to the same variable shorthand 
// += means adding some value to the existing value 
price1 += 5;
console.log(price1);

// -= means deducting some values from the current value of the var 
price2 -= 5;
console.log(price2);

// *=
price1 *= 2;

// /=
price1 /= 2;
console.log(price1);


// ++ means adding 1 to the existing value
var age = 15;
age++;
console.log(age);

// -- means deducting 1 from the existing Value
var love = 100;
love--;
console.log(love);

shorthand
// ++ is increment operator and -- is called decrement operator.